typedef struct HccASTExpr HccASTExpr;
struct HccASTExpr {
	union {
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
		};
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccDecl         decl;
		} function;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTUnaryOp   op;
			HccASTExpr*     expr;
		} unary;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTBinaryOp  op;
			bool            is_assign; // add/sub/mul/div assignment
			HccASTExpr*     left_expr;
			union {
				HccASTExpr* right_expr;
				uint32_t    field_idx;
			};
		} binary;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccConstantId   id;
		} constant;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     first_stmt;
			HccASTExpr*     last_stmt;
		} stmt_block;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     expr;
		} return_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     expr;
		} cast_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     first_expr;
		} curly_initializer;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     value_expr;
			uint32_t        elmt_indices_start_idx; // index into HccAST.designated_initializer_elmt_indices
			uint32_t        elmts_count;
		} designated_initializer;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     cond_expr;
			HccASTExpr*     true_stmt;
			HccASTExpr*     false_stmt;
		} if_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     cond_expr;
			HccASTExpr*     block_expr;
			uint32_t        case_stmts_count: 31;
			uint32_t        has_default_case: 1;
		} switch_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     next_case_stmt;
			HccConstantId   constant_id;
		} case_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     cond_expr;
			HccASTExpr*     loop_stmt;
		} while_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     init_stmt;
			HccASTExpr*     cond_expr;
			HccASTExpr*     inc_stmt;
			HccASTExpr*     loop_stmt;
		} for_;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccDecl         decl;
		} variable;
		struct {
			HccASTExprType  type: 7;
			uint8_t         is_stmt: 1;
			HccASTExpr*     next_case_stmt;
			HccASTExpr*     expr;
			HccDataType     data_type;
		} generic_case;
	};

	HccLocation* location;
	HccASTExpr* next_stmt;
	HccDataType data_type;
};

