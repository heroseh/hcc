#define HCC_AML_INTRINSIC_DATA_TYPE_IS_VECTOR(type)  (HCC_AML_INTRINSIC_DATA_TYPE_COLUMNS(type) > 1 && HCC_AML_INTRINSIC_DATA_TYPE_ROWS(type) == 1)
